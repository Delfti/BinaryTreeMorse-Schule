public class Buchstabe {
  private String buchstabe;
  private String code;

  public Buchstabe(String pBuchstabe, String pCode) {
    this.buchstabe = pBuchstabe;
    this.code = pCode;
  }

  public String getBuchstabe() {
    return this.buchstabe;
  }

  public String getCode() {
    return this.code;
  }
}